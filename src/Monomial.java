public class Monomial {

    private  int coefficient;
    private  int exponent;

    public Monomial(int c, int e) {
        coefficient = c;
        exponent = e;
    }

    public int getCoefficient() {
        return coefficient;
    }

    public int getExponent() {
        return exponent;
    }

}
